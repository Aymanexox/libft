#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	len;

	len = 0;
	if (s == NULL || fd < 0) 
		return ;
	while (s[len] != '\0')
	{
		len++;
	}
	write(fd, s, len);
}
